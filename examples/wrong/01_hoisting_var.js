/**
 * 호이스팅 퀴즈 #1 - var 호이스팅
 *
 * 문제:
 * console.log(a);
 * var a = 10;
 * console.log(a);
 *
 * 오답: 10, 10
 * 정답: undefined, 10
 */

// ============================================
// 핵심 개념: var는 "선언만" 호이스팅된다
// ============================================

// 우리가 작성한 코드
console.log(a);
var a = 10;
console.log(a);

// JS 엔진이 실제로 해석하는 코드:
// var a;           // 선언만 끌어올려짐 (호이스팅)
// console.log(a);  // undefined (선언됐지만 값이 없음)
// a = 10;          // 할당은 원래 위치에 남음
// console.log(a);  // 10

// ============================================
// 호이스팅이란?
// ============================================
// - 변수와 함수 선언이 코드 최상단으로 끌어올려지는 것처럼 동작
// - var: 선언만 호이스팅, 할당은 X → 초기값 undefined
// - let/const: 호이스팅되지만 TDZ(Temporal Dead Zone)에 의해 접근 불가
// - function 선언문: 선언 + 함수 전체가 호이스팅

// ============================================
// 추가 예제: 선언만 있는 경우
// ============================================

A = 1;
console.log(A);  // 1
var A;

// JS 엔진이 해석하는 순서:
// var A;           // 선언이 호이스팅
// A = 1;           // 할당
// console.log(A);  // 1

// 핵심: var A; 는 이미 값이 있는 변수를 "재선언"만 하는 것
// var A = undefined; 가 아니라 그냥 var A; (선언만)
// 따라서 기존 값(1)을 덮어쓰지 않음

